
public class problem {
	
	private String type;
	private int amt;
	public problem(String type, int amt){
		
		this.type = type;
		this.amt = amt;
		
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getAmt() {
		return amt;
	}
	public void setAmt(int amt) {
		this.amt = amt;
	}
	


}
